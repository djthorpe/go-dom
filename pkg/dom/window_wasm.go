//go:build js

package dom

import (
	"syscall/js"

	"github.com/djthorpe/go-dom"
)

///////////////////////////////////////////////////////////////////////////////
// TYPES

type window struct {
	js.Value
}

///////////////////////////////////////////////////////////////////////////////
// GLOBALS

var (
	w = &window{js.Global()}
)

///////////////////////////////////////////////////////////////////////////////
// LIFECYCLE

func NewWindow() dom.Window {
	return w
}

///////////////////////////////////////////////////////////////////////////////
// PROPERTIES

func (this *window) Document() dom.Document {
	return NewNode(w.Get("document")).(dom.Document)
}


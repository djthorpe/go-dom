package mvc

///////////////////////////////////////////////////////////////////////////////
// TYPES

// div is a simple div view
type div struct {
	View
}

///////////////////////////////////////////////////////////////////////////////
// GLOBALS

const (
	ViewDiv = "wasmbuild-mvc-div"
)

func init() {
	RegisterView(ViewDiv, nil)
}

///////////////////////////////////////////////////////////////////////////////
// LIFECYCLE

func Div() *div {
	view := &div{
		NewView(ViewDiv, "DIV"),
	}
	return view
}
